DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS requests CASCADE;
DROP TABLE IF EXISTS items CASCADE;
DROP TABLE IF EXISTS booking CASCADE;

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR(255) NOT NULL,
    email VARCHAR(512) NOT NULL,
    CONSTRAINT pk_user PRIMARY KEY (id),
    CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
    );

CREATE TABLE IF NOT EXISTS items (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(40),
    description VARCHAR(255),
    available BOOLEAN,
    owner_id INT,
    request_id INT,
    last_booking TIMESTAMP WITHOUT TIME ZONE,
    next_booking TIMESTAMP WITHOUT TIME ZONE,
    FOREIGN KEY (owner_id) REFERENCES users(id) ON DELETE CASCADE,
    FOREIGN KEY (request_id) REFERENCES users(id) ON DELETE CASCADE
    );

CREATE TABLE IF NOT EXISTS bookings
(
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    start_date TIMESTAMP WITH TIME ZONE,
    end_date TIMESTAMP WITH TIME ZONE,
    item_id INT REFERENCES items (id) ON DELETE CASCADE,
    booker_id INT REFERENCES users (id) ON DELETE CASCADE,
    owner_id INT REFERENCES users (id) ON DELETE CASCADE,
    status VARCHAR(30),
    available BOOLEAN
    );

CREATE TABLE IF NOT EXISTS requests
(
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    description VARCHAR(50),
    requestor_id INT REFERENCES users (id) ON DELETE CASCADE
    );

CREATE TABLE IF NOT EXISTS comments
(
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    text VARCHAR(200),
    item_id INT REFERENCES items (id) ON DELETE CASCADE,
    author_id INT REFERENCES users (id) ON DELETE CASCADE,
    created TIMESTAMP WITHOUT TIME ZONE
    );